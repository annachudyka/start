package pl.anna.lesson1.exercise;

public class Features {

    private String eyes;
    private double height;
    private String sex;

    public Features(String eyes, double height, String sex) {
        this.eyes = eyes;
        this.height = height;
        this.sex = sex;
    }

    public String getEyes() {
        return eyes;
    }

    public double getHeight() {
        return height;
    }

    public String getSex() {
        return sex;
    }
}
